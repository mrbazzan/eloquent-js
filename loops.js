
// while loop
let number = 0
while (number <= 12){
    console.log(number);
    number = number + 2
}

// do-while loop
let the_name;
do{
    the_name = prompt("Who are you? ");
} while (!the_name);
console.log("Hello, " + the_name)

// for loop
for (let number = 0; number <= 12; number = number + 2){
    console.log(number);
}